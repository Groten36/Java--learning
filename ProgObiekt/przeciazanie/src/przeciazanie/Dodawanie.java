public class Dodawanie {
	public void dodaj(int a, float b) {
		System.out.print(a+b);
	}
	public int dodaj(int a,int b, int c) {
		return a+b+c;
	}
	public double dodaj(double a, double b, int c, int d) {
		return a+b+c+d;
	}
	
	public float dodaj(float a,float b, int c, float d, int e) {
		return a+b+c+d+e;
	}

}
